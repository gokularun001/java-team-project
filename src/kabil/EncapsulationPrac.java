package kabil;

public class EncapsulationPrac {
    private String name;
    private int age;

    public void setName(String name1) {
        name = name1;
    }

    public String getname() {
        return name;
    }
    public void setAge(int newAge){
        age =  newAge;
    }
    public int getAge(){
        return age;
    }

}