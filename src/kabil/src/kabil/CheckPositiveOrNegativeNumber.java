package kabil;

public class CheckPositiveOrNegativeNumber {
    public static void main(String[] args){

        int num1 = 0;
        if (num1 > 0){
            System.out.println("the number is positive");

        } else if (num1 < 0){
            System.out.println("the number is negative");
        }
        else {
            System.out.println("the number is zero");
        }
    }
}
